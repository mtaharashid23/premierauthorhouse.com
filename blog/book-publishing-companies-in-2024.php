                  <p>Founded in 2007, <a class="mylink" target="_blank" href="https://www.cengage.com/"> Cengage </a>is
                     a prominent publishing company in the educational content and technology sector, committed to
                     improving learning experiences. With operations spanning across more than 20 countries globally,
                     Cengage has a significant presence as a global player in educational publishing.</p>
                  <p><b>Genres:</b> Academic, research journals, textbooks</p>
