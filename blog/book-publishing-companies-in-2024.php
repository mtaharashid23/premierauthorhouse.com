                     series like "Attack on Titan," "Sailor Moon," and "Akira" have achieved global recognition under
                     Kodansha's banner. The company's commitment to quality and innovation has cemented its status as a
                     powerhouse in the publishing industry, making it a key player in both Japanese and international
                     markets.</p>
