                     manga, novels, non-fiction, and magazines. Specializing in popular manga magazines such as
                     Nakayoshi, Afternoon, Weekly Shōnen Magazine, and Bessatsu Shōnen Magazine, Kodansha holds a
                     dominant position in the manga industry.</p>
